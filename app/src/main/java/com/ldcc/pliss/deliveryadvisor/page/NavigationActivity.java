    private void addAllShippingMarkerList(){
        if(myMarker!=null && myMarker.length>0){
            for(int i =0 ; i<myMarker.length ;i++){
                myMarker[i].remove();
            }
        }

        RealmResults<Delivery> results = deliveryHelper.getAllDeliveryList();
        myMarker = new Marker[results.size()];

        LatLngBounds.Builder builder = new LatLngBounds.Builder();

        for(int i=0; i<results.size() ;i++){
            destinations.add(new LatLng(Double.parseDouble(results.get(i).getRECV_ADDR_LAT()),Double.parseDouble(results.get(i).getRECV_ADDR_LNG())));

            myMarker[i] = myMap.addMarker(new MarkerOptions()
                    .title(results.get(i).getITEM_NM())
                    .snippet(results.get(i).getRECV_ADDR())
                    .position(destinations.get(i))
                    .icon(BitmapDescriptorFactory.defaultMarker(new Random().nextInt(360))));
            builder.include(destinations.get(i));
        }

        LatLngBounds bounds = builder.build();

        int width = getResources().getDisplayMetrics().widthPixels;
        int height = getResources().getDisplayMetrics().heightPixels;
        int padding = (int) (width * 0.10); // offset from edges of the map 10% of screen

        CameraUpdate cu = CameraUpdateFactory.newLatLngBounds(bounds, width, height, padding);

        myMap.animateCamera(cu);
    }

    private BitmapDescriptor bitmapDescriptorFromVector(Context context, int vectorResId) {
        Drawable vectorDrawable = ContextCompat.getDrawable(context, vectorResId);
        vectorDrawable.setBounds(0, 0, vectorDrawable.getIntrinsicWidth(), vectorDrawable.getIntrinsicHeight());
        Bitmap bitmap = Bitmap.createBitmap(vectorDrawable.getIntrinsicWidth(), vectorDrawable.getIntrinsicHeight(), Bitmap.Config.ARGB_8888);
        Canvas canvas = new Canvas(bitmap);
        vectorDrawable.draw(canvas);
        return BitmapDescriptorFactory.fromBitmap(bitmap);
    }
